/*
em javascript, usamos variaveis para armazenar dados

    var : var e a forma antiga de declarar variaveis. o scopo pode ser "global" ou "local a uma função"

    let : Introduzida no ECMAScript 6 (ES6), permite declarar variáveis com escopo de bloco, ou seja, só existem dentro de um bloco específico ({}). Usada para variáveis que podem mudar.

    const : Também introduzida no ES6, é usada para variáveis que não podem ser reatribuídas.

*/

//exemplos
var nome = "João";
// primeiro a palavra chave da variavel e depois o nome dela e então um "="(no js nos chamamso o "=" sozinho de recebe) e então o valor da variavel

let nome = 30;
// basicamente a mesma syntax do "var" as vareaveis podem receber diferentes tipos de "valores" (veremso isso mais pra frente)

const pais = "Brasil";
// mesma coisa das outras a diferença e que uma "const" não pode ser modificada (veremos isso mais para frente)
