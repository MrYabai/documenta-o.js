/*
operação de atribuição : ele e usado para atribuir valores a variaveis
Atribuição simples: =;
Atribuição com soma: +=;
Atribuição com subtração: -=;
Atribuição com multiplicação: *=;
Atribuição com divisão: /=;
*/
let n = 0; // atribuição simples
console.log(n);
n += 3; //atribuição com soma
console.log(n);
n -= 1; //atribuição com subtração
console.log(n);
n *= 5; //atribuição com multiplicação
console.log(n);
n /= 5; //atribuição com divisão
console.log(n);