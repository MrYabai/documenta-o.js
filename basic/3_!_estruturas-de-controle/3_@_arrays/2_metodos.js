/*
metodos comuns de arrays

.push() : adiciona um elemento ao final do array
.pop() : remove o ultimo elemento do array
.shift() : remove o primeiro elemento do array
.unshift() : adiciona um elemento no inicio do array
.lenght : retorna o numero de elementos no array

ex : 
*/
let numeros = [1,2,3,4,5,6,7];
numeros.push(8); //adiciona o 4 no final do array
console.log(numeros);

numeros.pop(); //remove o ultimo elemento que no caso e o 8
console.log(numeros);

console.log(numeros.lenght);

